function revo(){
    Swal.fire({
        title: "Do you want to Continue To The Next Page?",
        showDenyButton: true,
        confirmButtonText: "Yes",
        denyButtonText: `No`,
        color: "white",
        background: "black",
      }).then((result) => {
        if (result.isConfirmed) {
            Swal.fire({
                width: 400,
                icon:"success",
                title: "Successfully Login To The Main Page",
               confirmButtonText: "OK",
               color: "blue",
               background: "black",
            })
            setTimeout(function () {
                window.location.href = "revo.html";
              }, 2000);
        } else if (result.isDenied) {
            Swal.fire({
                width: 400,
                icon:"error",
                title: "Not Login To The Main Page",
               showconfirmButton: true,
               confirmButtonText: "OK",
               color: "blue",
               background: "black",
            })
        }
      });
}



